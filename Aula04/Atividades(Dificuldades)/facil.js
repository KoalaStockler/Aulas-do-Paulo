/*Escreva um programa para verificar se o número fornecido pelo usuário é positivo, negativo ou zero.*/ 
numero = 10
if (numero > 0){
    console.log ("Positvo")
} else if (numero == 0){
    console.log ("Zero")
} else {
    console.log ("Negativo")
}

